package com.vn.hcmute.team.cortana.mymoney;

public class HelloModel {
	public String content;
	public long id;
	
	
	public HelloModel(){
		this.content="empty";
		this.id=-1L;
	}

	public String getContent() {
		return content;
	}


	public void setContent(String content) {
		this.content = content;
	}


	public long getId() {
		return id;
	}


	public void setId(long id) {
		this.id = id;
	}
	

}
